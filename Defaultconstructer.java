import java.util.*;
class Defaultconstructer
{
int age;
String name;
Defaultconstructer()
{
age=19;
name="lekhana";
}
public static void main(String args[])
{
Defaultconstructer obj=new Defaultconstructer();
System.out.println("name is:"+obj.name);
System.out.println("age is:"+obj.age);
}
}
